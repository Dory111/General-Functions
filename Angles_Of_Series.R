#===============================================================================
# Finds the angles
#===============================================================================
Angles_Of_Series <- function(series,
                             timestep_value = 1)
{
  #-------------------------------------------------------------------------------
  # Vector error
  tryCatch({
    series <- as.vector(unlist(series))
  }, error = function(e){
    stop('\nAngles_Of_Series: DATA NOT IN FORM OF VECTOR')
  })
  #-------------------------------------------------------------------------------
  
  
  #-------------------------------------------------------------------------------
  # Numeric error
  tryCatch({
    series <- as.numeric(series)
  }, error = function(e){
    stop('\nAngles_Of_Series: DATA NOT IN FORM OF NUMERIC')
  })
  #-------------------------------------------------------------------------------
  
  
  #-------------------------------------------------------------------------------
  angles <- c()
  x <- timestep_value
  for(i in 1:length(series))
  {
    #-------------------------------------------------------------------------------
    if(i == length(series)){
    }else{
      y <- series[i+1] - series[i]
      angles <- append(angles,Rad_To_Deg(atan((y/x))))
    }
    #-------------------------------------------------------------------------------
  }
  #-------------------------------------------------------------------------------
  return(angles)
}
