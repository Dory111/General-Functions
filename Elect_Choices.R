#===========================================================================================
# This runs all the functions and generates the graph
#===========================================================================================
Elect_Choices <- function()
{
  #------------------------------------------------------------------------------- 
  # dependencies
  username <- Sys.info()[["user"]]
  dropbox_dir <- paste0("C:/Users/",username,"/LWA Dropbox/")
  functions_dir <- paste0(dropbox_dir,"Christopher Dory/","Universal Code/")
  setwd(functions_dir)
  source(file.path('Set_Choices.R')) 
  #-------------------------------------------------------------------------------
  
  
  
  #------------------------------------------------------------------------------- 
  choice <- F
  while(choice == F)
  {
    #------------------------------------------------------------------------------- 
    Set_Default_Data('Multiple') # sets the default data choices
    Ask_For_Dataset_Type() # is the data spatial, timeseries, or scatter
    Ask_For_Continuous_Categorical() # should the colors represent continuous or categorical
    Set_Palettes()
    choice <- readline('Are these the correct choices? (T/F)')
    #------------------------------------------------------------------------------- 
    
    
    #------------------------------------------------------------------------------- 
    if(choice == 't')
    {
      choice <- 'T'
    }
    #------------------------------------------------------------------------------- 
    
    
    #------------------------------------------------------------------------------- 
    if(choice == 'f')
    {
      choice <- 'F'
    }
    #------------------------------------------------------------------------------- 
    
    
    #------------------------------------------------------------------------------- 
    if(is.na(as.logical(choice)) == T)
    {
      choice <- F
      cat('Input a logical operator (T/F)\n\n')
    }
    #------------------------------------------------------------------------------- 
    
    
    #------------------------------------------------------------------------------- 
    if(is.na(as.logical(choice)) == F)
    {
      choice <- as.logical(choice)
    }
    #------------------------------------------------------------------------------- 
  }
  Plot_Choices() 
  #------------------------------------------------------------------------------- 
}
