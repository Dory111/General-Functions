#===============================================================================
# For every object 1 pastes every object 2
# for example Paste_Len(c(1,2,3,4),c('a','b','c','d'))
# would become (1a,1b,1c,1d,2a,2b,2c,2d,3a,3b,3c,3d,4a,4b,4c,4d)
# objects do not need to be of equal length
#===============================================================================
Paste_Len <- function(obj1, obj2, sep = ' ')
{ 
  #-------------------------------------------------------------------------------
  new_obj <- c()
  for(i in 1:length(obj1))
  {
    new_obj <- append(new_obj, paste(obj1[i],obj2, sep = sep))
  }
  #-------------------------------------------------------------------------------
  return(new_obj)
}
