#===============================================================================
# This function sets custom gridlines using user specified locations
#===============================================================================
Set_Custom_Gridlines <- function(x = c(),
                                 y = c(),
                                 color = 'gray',
                                 lty = 3,
                                 lwd = 1)
{
  
  #-------------------------------------------------------------------------------
  for(x in x)
  {
    
    abline(v = x,
           lty = lty,
           lwd = lwd,
           col = color)
    
  }
  #-------------------------------------------------------------------------------
  
  
  #-------------------------------------------------------------------------------
  for(y in y)
  {
    
    abline(h = y,
           lty = lty,
           lwd = lwd,
           col = color)
    
    
  }
  #-------------------------------------------------------------------------------
}
