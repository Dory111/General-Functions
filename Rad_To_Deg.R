#===============================================================================
# Takes value or vector of degrees and returns radians
#===============================================================================
Rad_To_Deg <- function(rad)
{
  #-------------------------------------------------------------------------------
  # Vector error
  tryCatch({
    rad <- as.vector(unlist(rad))
  }, error = function(e){
    stop('\nRad_To_Deg: DATA NOT IN FORM OF VECTOR')
  })
  #-------------------------------------------------------------------------------
  
  
  #-------------------------------------------------------------------------------
  # Numeric error
  tryCatch({
    rad <- as.numeric(rad)
  }, error = function(e){
    stop('\nRad_To_Deg: DATA NOT IN FORM OF NUMERIC')
  })
  #-------------------------------------------------------------------------------
  
  #-------------------------------------------------------------------------------
  for(i in 1:length(rad))
  {
    rad[i] <- rad[i] * (180/3.14159)
  }
  #-------------------------------------------------------------------------------
  return(rad)
}
