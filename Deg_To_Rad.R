#===============================================================================
# Takes value or vector of degrees and returns radians
#===============================================================================
Deg_To_Rad <- function(deg)
{
  #-------------------------------------------------------------------------------
  # Vector error
  tryCatch({
    deg <- as.vector(unlist(deg))
  }, error = function(e){
    stop('\nDeg_To_Rad: DATA NOT IN FORM OF VECTOR')
  })
  #-------------------------------------------------------------------------------
  
  
  #-------------------------------------------------------------------------------
  # Numeric error
  tryCatch({
    deg <- as.numeric(deg)
  }, error = function(e){
    stop('\nDeg_To_Rad: DATA NOT IN FORM OF NUMERIC')
  })
  #-------------------------------------------------------------------------------

  #-------------------------------------------------------------------------------
  for(i in 1:length(deg))
  {
    deg[i] <- deg[i] * (3.14159/180)
  }
  #-------------------------------------------------------------------------------
  return(deg)
}
