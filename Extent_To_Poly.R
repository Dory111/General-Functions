#===========================================================================================
# Allows for st intersection on extents
# Must be extent from raster package
#===========================================================================================
Extent_To_Poly <- function(extent,
                           crs = 4326)
{
  #-------------------------------------------------------------------------------
  # Null dataframe to map onto
  df <- as.data.frame(matrix(nrow = 1, ncol = 3))
  colnames(df) <- c('Buff','Lon','Lat')
  df$Lon[1] <- -119
  df$Lat[1] <- 42
  df <- st_as_sf(df,
                 coords = c('Lon','Lat'),
                 crs = crs)
  #-------------------------------------------------------------------------------
  
  
  #-------------------------------------------------------------------------------
  df$geometry[1] <- st_polygon(list(cbind(c(extent[1],extent[1],extent[2],extent[2],extent[1]),
                                          c(extent[3],extent[4],extent[4],extent[3],extent[3]))))
  #-------------------------------------------------------------------------------
  
  return(df)
}
